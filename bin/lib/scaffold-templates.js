/**
 * Shared [proxy].edge.js for the KV presets/quickstart. It serves a few
 * sample routes itself (with a Cache-Control header so the Cache API will
 * retain them, and a unique per-response token so cache HIT/MISS is visible)
 * and passes anything else through to your app. The KV redirect/rewrite/cache
 * logic lives in the dev-worker shim, not here — so this stays production-safe.
 */
export const kvSampleHandlerTemplate = `
import { jsonResponse, passThrough } from "@aryanbansal-launch/edge-utils";

/** Local KV preset handler (generated by launch-init local) */
export default async function handler(request, context) {
  const url = new URL(request.url);
  const token = crypto.randomUUID();

  if (url.pathname === "/api/edge-ping") {
    return jsonResponse({ ok: true, source: "edge-utils", path: url.pathname });
  }

  // Sample HTML routes. Cache-Control makes them cacheable; the token differs
  // on every fresh render, so a cached (HIT) response is visibly identical.
  const html = (title) =>
    new Response(
      \`<!doctype html><title>\${title}</title><h1>\${title}</h1><p>token: \${token}</p>\`,
      {
        headers: {
          "Content-Type": "text/html; charset=utf-8",
          "Cache-Control": "public, max-age=3600"
        }
      }
    );

  switch (url.pathname) {
    case "/":
      return html("Home");
    case "/about":
      return html("About");
    default:
      if (url.pathname.startsWith("/blog/") || url.pathname.startsWith("/shop/")) {
        return html("Page " + url.pathname);
      }
  }

  return passThrough(request);
}
`.trim();

